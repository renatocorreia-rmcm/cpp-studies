#include <iostream>
#include <cmath>  // another header file

//https://cplusplus.com/reference/cmath/

int main(){
    std::cout << std::max(3, 5) << '\n';
    std::cout << std::min(3, 5) << '\n';

    std::cout << pow(12, 2) << '\n';
    std::cout << sqrt(256) << '\n';

    std::cout << abs(-3) << '\n';

    std::cout << round(1.618) << '\n';
    std::cout << floor(1.618) << '\n';

    return 0;
}